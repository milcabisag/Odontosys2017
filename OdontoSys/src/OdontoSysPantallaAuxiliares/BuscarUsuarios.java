    private void setearTabla() {
    
        tabla.addColumn("Id");
        tabla.addColumn("Usuario");
        tabla.addColumn("Observaci√≥n");
      
        lista = UsuarioControlador.BuscarTodosUsuarios();
        for (Usuario u : lista){
            Object[] f = new Object[3];
            f[0] = u.getIdusuario();
            f[1] = u.getNombre();
            f[2] = u.getObservacion();
            tabla.addRow(f);
        }
    
    }
