    private void inicializarTabla() {
        tabla.addColumn("Cod Orden");
        tabla.addColumn("Cod Odontograma");
        tabla.addColumn("Fecha");
    }

    private void realizarConsulta() {
        lista = OrdenServicioControlador.BuscarOrdenPendiente(elPaciente.getIdPaciente());
        if(lista != null){
        for(OrdenServicio nuevo : lista){
                
                Object[] rowData = new Object[3];
                rowData[0] = nuevo.getIdordenServicio();
                rowData[1] = nuevo.getOdontograma().getIdodontograma();
                rowData[2] = nuevo.getFecha();
                
                tabla.addRow(rowData);
            }
        }
    }
    
    private void irOrden() {
        OrdenDeServicio jFrame= new OrdenDeServicio();
        jFrame.setVisible(true); //Abre Form
        this.setVisible(false);
    }
