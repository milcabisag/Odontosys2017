    private void obtenerNuevo() {
        if(jDateChooserFecha.getDate() != null && jTextFieldResumenHallazgos.getText() != null && jTextFieldPlanTratamiento.getText() != null){
            nuevo = new Diagnostico();
            nuevo.setTipo(jComboBoxTipo.getSelectedItem().toString());
            nuevo.setFecha(jDateChooserFecha.getDate());
            nuevo.setEmpresa(jTextFieldEmpresa.getText());
            nuevo.setResumenHallazgos(jTextFieldResumenHallazgos.getText());
            nuevo.setPlanTratamiento(jTextFieldPlanTratamiento.getText());
            nuevo.setObservaciones(jTextFieldObservaciones.getText());
            nuevo.setPaciente(pacienteActual);
            nuevo.setOdontograma(odont);
            nuevo.setUsuario(user);
        }else{
            JOptionPane.showMessageDialog(null,"Favor complete todos los campos", "Validar Diagnóstico", WIDTH );
        }
        
    }

