    private void limpiar() {
        
        Configuraciones.limpiarModelo(tabla);
        jTextFieldTipo.setText("Tipo");
        jTextFieldTipo.setText("");
        pacActual = null;
        empresaActual = null;
        emp = null;
        pac = null;
    }

    private void crearTablaPaciente() {
        tabla.addColumn("Paciente");
        tabla.addColumn("Cédula");
        tabla.addColumn("Teléfonos");
    }
    
    private void crearTablaEmpresa() {
        tabla.addColumn("Empresa");
        tabla.addColumn("RUC");
        tabla.addColumn("Teléfono");
    }

    private void deshabilitarBotones() {
        jButtonBuscar.setVisible(false);
        jButtonInsertar.setVisible(false);
    }

    private void escribirPaciente(Paciente pacActual) {
        jLabelTipo.setText("Paciente");
        jTextFieldTipo.setText(pacActual.getNombres()+" "+pacActual.getApellidos());
        crearTablaEmpresa();
        
        emp = new ArrayList();
        emp = ConvenioControlador.BuscarConvenioEmpresa(pacActual);
        if(emp != null){
            for(Empresa e : emp){
                Object[] emp = new Object[3];
                emp[0] = e.getNombre();
                emp[1] = e.getRuc();
                emp[2] = e.getTelefono();
                tabla.addRow(emp);
            }
        }
    }

    private void escribirEmpresa(Empresa empresaActual) {
    
        jLabelTipo.setText("Empresa");
        jTextFieldTipo.setText(empresaActual.getNombre());
        crearTablaPaciente();
        
        pac = new ArrayList();
        pac = ConvenioControlador.BuscarPacientesConConvenios(empresaActual);
        if(pac != null){
            for(Paciente p : pac){
                Object[] pa = new Object[3];
                pa[0] = p.getNombres()+" "+p.getApellidos();
                pa[1] = p.getIdPaciente();
                pa[2] = p.getTelCel()+"     "+p.getTelLb();
                tabla.addRow(pa);
            }
        }
    
    }    
    
    private int obtenerCedula() {
        int cedula = -1;
        boolean bandera;
            do{
                try{
                    String cedulaString = JOptionPane.showInputDialog ( null, "Ingrese CI de Paciente" , "Buscar Convenio del Paciente" , JOptionPane.QUESTION_MESSAGE ) ;        
                    if(cedulaString == null){
                        bandera = true;
                    }else{
                        cedula= Integer.parseInt(cedulaString);
                        bandera = true;
                    }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Solo se permiten numeros" , "Buscar por CI" , JOptionPane.QUESTION_MESSAGE );
                bandera = false;
                cedula = -1;
                }
            }while(!bandera);
        return cedula;
    }

    
