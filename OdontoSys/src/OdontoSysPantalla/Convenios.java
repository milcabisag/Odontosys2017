    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed

        String busq = JOptionPane.showInputDialog(null, "Buscar por", "Tipo de Búsqueda", 
            JOptionPane.QUESTION_MESSAGE, null, new Object[] {"Paciente", "Empresa"}, 0).toString();
        
        if(busq.compareTo("Paciente") == 0){      //Buscar por cédula del paciente
            int cedula = obtenerCedula();
            if(cedula > 0){
            pacActual = PacienteControlador.BuscarCedula(cedula);
            if (pacActual != null) {
                deshabilitarBotones();
                escribirPaciente(pacActual);
            } else {
                JOptionPane.showMessageDialog(null, "Paciente no encontrado" , "Búsqueda por Paciente" , JOptionPane.QUESTION_MESSAGE );
            } 
        }
        }else{              //Buscar por empresa
            ObtenerEmpresa jDialog= new ObtenerEmpresa(null, true);
            jDialog.setVisible(true);
            empresaActual = jDialog.getReturnStatus();
            if(empresaActual != null){
                deshabilitarBotones();
                escribirEmpresa(empresaActual);
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo recuperar la empresa" , "Búsqueda por Empresa" , JOptionPane.QUESTION_MESSAGE );
            } 
        }
        
        
        
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed

