    private void jTableConveniosMouseClicked(java.awt.event.MouseEvent evt) {                                             
        int fila = jTableConvenios.getSelectedRow();
        if(empresaActual == null){      //Búsqueda por paciente
            Empresa e = new Empresa();
            e = emp.get(fila);
            DetalleConvenio.pacActual = pacActual;
            DetalleConvenio.empresaActual = e;
        }else{                          //Búsqueda por empresa
            Paciente p = new Paciente();
            p = pac.get(fila);
            DetalleConvenio.empresaActual = empresaActual;
            DetalleConvenio.pacActual = p;
        }
        DetalleConvenio jFrame = new DetalleConvenio();
        jFrame.setVisible(true);
        
    }
    
