    private String calcularMes(int mes) {
        String letras =  null;
        if(mes == 1){
            letras = "Enero";
        }else if(mes == 2){
            letras = "Febrero";
        }else if(mes == 3){
            letras = "Marzo";
        }else if(mes == 4){
            letras = "Abril";
        }else if(mes == 5){
            letras = "Mayo";
        }else if(mes == 6){
            letras = "Junio";
        }else if(mes == 7){
            letras = "Julio";
        }else if(mes == 8){
            letras = "Agosto";
        }else if(mes == 9){
            letras = "Setiembre";
        }else if(mes == 10){
            letras = "Octubre";
        }else if(mes == 11){
            letras = "Noviembre";
        }else if(mes == 12){
            letras = "Diciembre";
        }
        return letras;
    }
