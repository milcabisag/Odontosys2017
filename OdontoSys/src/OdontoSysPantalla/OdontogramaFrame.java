    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        // TODO add your handling code here:
        int cantRowHallazgo = modeloH.getRowCount();
        int cantRowTratamiento = modeloT.getRowCount();
        int fila;
        int cantActual = 0;
        if(tipoTransaction == 1 && cantRowHallazgo > 0){
            fila = jTableContenedorDatos.getSelectedRow();
            modeloH.removeRow(fila);
            hallazgosSet.remove(fila);
            
            cantActual = modeloH.getRowCount(); 
            jButtonEliminar.setVisible(false);
        }else if(tipoTransaction == 2 && cantRowTratamiento > 0){
            fila = jTableContenedorDatos.getSelectedRow();
            modeloT.removeRow(fila);
            listaTratamientos.remove(fila);
            servicios.remove(fila);
            
            cantActual = modeloT.getRowCount();  
            jButtonEliminar.setVisible(false);          
        }else{
            JOptionPane.showMessageDialog(null, "No hay qu√© Borrar", "Borra Hallazgo/Tratamiento", ERROR);
        }
        
        if(cantActual <= 0){
            jButtonEliminar.setEnabled(false);
            jButtonGuardarOdonto.setEnabled(false);
        }else{
            jButtonGuardarOdonto.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

