    private void cargarTablaHallazgo(DetalleHallazgo nuevoHallazgo) {
        Object row[] = new Object[5];
        row[0] = nuevoHallazgo.getHallazgo().getNombre();
        row[1] = nuevoHallazgo.getDiente().getNombre();
        row[2] = nuevoHallazgo.getDiente().getNomenclatura();
        row[3] = nuevoHallazgo.getUbicacion();
        row[4] = nuevoHallazgo.getEspecificacion();
        
        modeloH.addRow(row);
    }
    
    private void cargarTablaTratamiento(Tratamiento nuevoTratamiento) {
        String row[] = new String[6];
        row[0] = nuevoTratamiento.getServicio().getDescripcion();
        row[1] = String.valueOf(nuevoTratamiento.getServicio().getPrecio());
        row[2] = nuevoTratamiento.getDiente().getNombre();
        row[3] = String.valueOf(nuevoTratamiento.getDiente().getNomenclatura());
        row[4] = nuevoTratamiento.getEstado();
        row[5] = nuevoTratamiento.getEspecificacion();

        modeloT.addRow(row);
    }
    
    private void procesarDiente(int nomDiente) {
        nuevoHallazgo = null;
        nuevoTratamiento = null;
        dienteActual = null;
        
        if(tipoTransaction == 1){
            
            dienteActual = DienteControlador.BuscarDiente(nomDiente);
            
            InsertarHallazgoAct InsertH = new InsertarHallazgoAct(this, true);
            InsertH.GraficarDiente(String.valueOf(nomDiente));
            InsertH.setVisible(rootPaneCheckingEnabled);
            nuevoHallazgo = InsertH.getReturnStatus();
            
            if(nuevoHallazgo != null){
                nuevoHallazgo.setDiente(dienteActual);
                cargarTablaHallazgo(nuevoHallazgo);
                hallazgosSet.add(nuevoHallazgo);
                jButtonGuardarOdonto.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(null, "No ha generado ning√∫n hallazgo", "Generar Hallazgo", JOptionPane.ERROR_MESSAGE);
            }
            
        }else if(tipoTransaction == 2){
            dienteActual = DienteControlador.BuscarDiente(nomDiente);
            
            InsertarTratamientoAct InsertT = new InsertarTratamientoAct(this, true);
            InsertT.GraficarDiente(String.valueOf(nomDiente));
            InsertT.setVisible(rootPaneCheckingEnabled);
            
            nuevoTratamiento = InsertT.getReturnStatus();
            
            if(nuevoTratamiento != null){
                nuevoTratamiento.setDiente(dienteActual);
                cargarTablaTratamiento(nuevoTratamiento);
                listaTratamientos.add(nuevoTratamiento);
                servicios.add(nuevoTratamiento.getServicio());
                jButtonGuardarOdonto.setEnabled(true);
            }else{
                JOptionPane.showMessageDialog(rootPane, "No ha Generado ningun Tratamiento", "Generar Tratamiento", JOptionPane.WARNING_MESSAGE);
            }    
        }else{
                JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un Tipo de Odontograma", "Setear Odontograma", JOptionPane.ERROR_MESSAGE);
        }
    }
    
