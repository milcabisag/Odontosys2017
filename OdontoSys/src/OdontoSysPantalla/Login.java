    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed

       String nombre = jTextFieldUsuario.getText();
       String passw = jTextFieldContraseña.getText();
       if(nombre.trim().compareTo("") != 0 && passw.trim().compareTo("") != 0){
            user = UsuarioControlador.Login(nombre, passw);
            if (user != null) {
                Inicio.usuario = user;
                Inicio.main(null);
                //Inicio jFrame= new Inicio(user);
                //jFrame.setVisible(true); //muestra el segundo jFrame
                this.dispose();//oculta el jFrame actual
            } else {
                JOptionPane.showMessageDialog(null, "Usuario no encontrado! Favor verifique sus datos" , "Login Controlador" , JOptionPane.QUESTION_MESSAGE );
                limpiar();
            }
       }else{
           JOptionPane.showMessageDialog(null, "Favor rellene ambos campos" , "Login" , JOptionPane.QUESTION_MESSAGE );
       }       
    }//GEN-LAST:event_jButtonLoginActionPerformed

